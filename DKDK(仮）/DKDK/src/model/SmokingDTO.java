package model;

public class SmokingDTO
{
	private int smoking_id;
	private String smoking;

	public int getSmokingId()
	{
		return this.smoking_id;
	}

	public void setSmokingId(int smokingId)
	{
		this.smoking_id = smokingId;
	}

	public String getSmoking()
	{
		return this.smoking;
	}

	public void setSmoking(String sm)
	{
		this.smoking = sm;
	}

}
